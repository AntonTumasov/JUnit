public class Math {
    public int sum(int a, int b){
        return a+b;
    }
    public String sum(String first, String second){
        return first + second;
    }
    public int mul(int x, int y){
        return x*y;
    }
    public float mul(float x, float y){
        return x*y;
    }
    public float func(float x){
        return 2/(10-x);
    }
}
